#include<stdio.h>
#include<conio.h>

//main() function begin

int main()
{
    int  n;
    float r, V = 0,P,x;

    for(P = 1000; P <= 10000; P+=1000)

    {
        printf("P = %.2f\t",P);
        for(r = 10; r <= 20; r++)
        {
            printf("r = %.2f\t\n",r/100);
            for(n=1, x = P ; n <= 10; n++)
            {

                V = x*(1+(r/100));
                printf("%.2f ",V);
                x = V;
            }
            printf("\n");

        }
    }
	getch();
	return 0;
}
///main() end

